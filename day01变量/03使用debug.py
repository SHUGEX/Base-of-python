# debug的使用步骤
# 1.打断点---》断点需要打在调试的代码块的第一行，代码左侧的空白区域
# 2.Debug调试---》针对程序运行之后，没有达到想要的效果

# 注意：使用变量时，必须要先定义变量，才能使用变量
# name = "maiya"          # 定义变量
# print(name)             # 使用变量

# 总结：第一种，debug用来处理肉眼看不见的错误，如果程序执行之后没有达到想要的效果，那就执行debug调试

# 直接报错：会有红色下划波浪线，并且控制台会提示错误的位置
# print(name)         # name 'name' is not defined，名字没有被定义
#
# 总结：第二种，所以肉眼可见的错误可以控制台的报错提示进行解决


